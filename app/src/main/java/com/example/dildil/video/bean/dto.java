package com.example.dildil.video.bean;

public class dto {
    private int num;
    private int vid;

    public dto(int num,int vid){
        this.num = num;
        this.vid = vid;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getVid() {
        return vid;
    }

    public void setVid(int vid) {
        this.vid = vid;
    }
}
