package com.example.dildil.home_page.bean;

public class VideoBean {
    private String imgurl;
    private String play_volume;
    private String barrage_volume;
    private String title;

    public String getImgurl() {
        return imgurl;
    }

    public void setImgurl(String imgurl) {
        this.imgurl = imgurl;
    }

    public String getPlay_volume() {
        return play_volume;
    }

    public void setPlay_volume(String play_volume) {
        this.play_volume = play_volume;
    }

    public String getBarrage_volume() {
        return barrage_volume;
    }

    public void setBarrage_volume(String barrage_volume) {
        this.barrage_volume = barrage_volume;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
